#include "3-calc.h"

/**
 * op_add - adds two numbers
 *
 * @a: first num
 * @b: second num
 *
 * Return: The sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 *
 * @a: num one
 * @b: num two
 *
 * Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multuplies two numbers
 *
 * @a: num one
 * @b: num two
 *
 * Return: The product
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 *
 * @a: num one
 * @b: num two
 *
 * Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modules two numbers
 *
 * @a: num one
 * @b: num two
 *
 * Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
